MERGE INTO seller_payout_recoveries recovery
USING (
    SELECT
        payment.id AS source_payment_transaction_id,
        payout.id AS payout_transaction_id,
        payment.seller_user_id,
        GREATEST(
            NVL(payout.requested_amount, 0)
                + NVL(payout.recovery_offset_amount, 0),
            0) AS original_paid_amount,
        GREATEST(NVL(payment.net_amount, 0), 0) AS target_net_amount,
        GREATEST(
            NVL(payout.requested_amount, 0)
                + NVL(payout.recovery_offset_amount, 0)
                - NVL(payment.net_amount, 0),
            0) AS recovery_amount
      FROM payment_transactions payment
      JOIN payout_transactions payout
        ON payout.payment_transaction_id = payment.id
       AND payout.status = 'COMPLETED'
       AND payout.failure_code = 'PAYOUT_COMPLETION_VERIFIED'
     WHERE payment.settlement_status = 'REFUNDED_AFTER_SETTLEMENT'
) source
ON (recovery.source_payment_transaction_id = source.source_payment_transaction_id)
WHEN NOT MATCHED THEN
    INSERT (
        source_payment_transaction_id,
        payout_transaction_id,
        seller_user_id,
        original_paid_amount,
        target_net_amount,
        recovery_amount,
        recovered_amount,
        status,
        created_at,
        updated_at
    )
    VALUES (
        source.source_payment_transaction_id,
        source.payout_transaction_id,
        source.seller_user_id,
        source.original_paid_amount,
        source.target_net_amount,
        source.recovery_amount,
        0,
        CASE WHEN source.recovery_amount > 0 THEN 'PENDING' ELSE 'RECOVERED' END,
        SYSTIMESTAMP,
        SYSTIMESTAMP
    );
