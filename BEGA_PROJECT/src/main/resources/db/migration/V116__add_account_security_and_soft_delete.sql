DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM user_tab_cols
     WHERE table_name = 'USERS'
       AND column_name = 'PENDING_DELETION';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE users ADD (pending_deletion NUMBER(1) DEFAULT 0 NOT NULL)';
    END IF;
END;
/

