DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM user_tables
     WHERE table_name = 'TRUSTED_DEVICES';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE
            'CREATE TABLE trusted_devices (
                id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
                user_id NUMBER NOT NULL,
                fingerprint VARCHAR2(128 CHAR) NOT NULL,
                device_label VARCHAR2(255 CHAR) NOT NULL,
                device_type VARCHAR2(32 CHAR) NOT NULL,
                browser VARCHAR2(64 CHAR) NOT NULL,
                os VARCHAR2(64 CHAR) NOT NULL,
                first_seen_at TIMESTAMP NOT NULL,
                last_seen_at TIMESTAMP NOT NULL,
                last_login_at TIMESTAMP NOT NULL,
                last_ip VARCHAR2(64 CHAR),
                revoked_at TIMESTAMP NULL
            )';
    END IF;
END;
/

BEGIN
    EXECUTE IMMEDIATE
        'ALTER TABLE trusted_devices
            ADD CONSTRAINT fk_trusted_devices_user
            FOREIGN KEY (user_id) REFERENCES users(id)';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -2264 THEN
            RAISE;
        END IF;
END;
/

BEGIN
    EXECUTE IMMEDIATE
        'CREATE UNIQUE INDEX uq_trusted_devices_user_fingerprint
            ON trusted_devices(user_id, fingerprint)';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;
/

BEGIN
    EXECUTE IMMEDIATE
        'CREATE INDEX ix_trusted_devices_user_last_seen
            ON trusted_devices(user_id, last_seen_at DESC)';
EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
            RAISE;
        END IF;
END;
